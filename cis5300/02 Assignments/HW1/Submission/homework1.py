def BPE_tokenize(text, merges_records):
    """
    Use the merges_records to tokenize the input text

    @param text: the input text to be tokenized
    @return: a list of tokens
    """
    # TODO: pre-tokenize the lowercased input text, think about which tokenizer to use
    text = text.lower()
    pattern = r'''(?x)          # set flag to allow verbose regexps
        (?:[A-Z]\.)+        # abbreviations, e.g. U.S.A.
      | \w+                 # words without support optional internal hyphens, the old ways: | \w+(?:-\w+)*
      | \$?\d+(?:\.\d+)?%?  # currency and percentages, e.g. $12.40, 82%
      | \.\.\.              # ellipsis
      | \!                  # exclation mark
      | [][.,;"'?():_`-]    # these are separate tokens; includes ], [
    '''
    tokens = nltk.regexp_tokenize(text, pattern)

    tokenized = []
    # go through tokens, only put all word in tokenized
    for token in tokens:
      if has_digit(token) == False and has_punctuation(token) == False and has_bad_letters(token) == False and is_blank(token) == False:
            tokenized.append(token)

    # TODO: split each token in the list to a list of characters
    # splits = []
    splits = [[l for l in token] for token in tokenized]
    # TODO: iterate over merges_records and apply all merge rules
    for pair, merged_pair in merges_records.items():
      for idx, split in enumerate(splits):
        i = 0
        while i < len(split) - 1:
            if split[i] == pair[0] and split[i + 1] == pair[1]:
                split = split[:i] + [merged_pair] + split[i + 2 :]
            else:
                i += 1
        splits[idx] = split

    return sum(splits, [])

